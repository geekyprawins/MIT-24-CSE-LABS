	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler

	LDR R0, N1
	LDR R1, N2
	LDR R2, =DST
	ADD R2, #3
	LDR R6, =0
	LDR R7, =2
	
LOOP
	ORRS R3, R0, R1
	BEQ DONE
	AND R4, R0, #0X0000000F
	AND R5, R1, #0X0000000F
	ADD R4, R5
	ADD R4, R6	
	LDR R6, =0
	CMP R4, #10
	SUBCS R4, #10
	ADDCS R6, #1
	SUBS R7, #1
	LSLEQ R4, #4
	ORREQ R4, R8
	STRBEQ R4, [R2], #-1
	LDREQ R7, =2
	MOVNE R8, R4	
	LSR R0, #4
	LSR R1, #4
	B LOOP

DONE
	CMP R7, #0
	STRBNE R8, [R2]
	
HERE
	B HERE

N1 DCD 0X255
N2 DCD 0X257
	
	AREA mydata, DATA, READWRITE
DST DCD 0
	END
	